package stringmetodos;

public class MetodoSubstring {

	public static void main(String[] args) {
		
		String frase = "A Linguagem Java";
		
		//Utilização do método subString() para fatiar strings
		System.out.println("Frase: "+frase);
		System.out.println("Substring(0,2): "+frase.substring(0, 2));
		System.out.println("Substring(3,8): "+frase.substring(3, 8));
		System.out.println("Substring(4):"+frase.substring(4));
		
		

	}

}
